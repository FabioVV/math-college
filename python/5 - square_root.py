from math import sqrt

val_one = float(input("Type in the first number: "))
val_two = float(input("Type in the second number: "))

print(f"Add: {val_one+val_two}")
print(f"Minus: {val_one-val_two}")
print(f"Mult: {val_one*val_two}")
print(f"SQRT one: {sqrt(val_one):.2f}")
print(f"SQRT two: {sqrt(val_two):.2f}")
